<p>O início desta ilustre banda remonta a 1967, quando Brian May, Tim Stafell e Roger Taylor formaram o trio Smile, onde estudavam em Londres. Após a saída de Tim Stafell em 1970, ele apresenta Farrokh Bulsara a May e Taylor e em abril do mesmo ano encontram Freddie Mercury para ser o vocalista da banda e a batizam com o nome “Queen”, em 1971 a banda fica completa quando John Deacon se torna integrante. </p>

<p>
    Todos os integrantes compunham, cantavam e tocavam, mas ainda sim foram recusados em diversos testes de produtoras, a carreira dos Queen se inicia de fato em 1973 com o lançamento do álbum que leva o nome da banda, “Queen”, com este álbum inicial a sua assinatura “sem sintetizadores”, que representa um som puro sem alterações.
    Porém a banda só alcançou o sucesso em seu segundo álbum, em 1974, “Queen II”, foi o quinto álbum mais vendido no Reino Unido, no mesmo ano lançaram “Sheer Heart Attack” que se tornou sucesso mundial, em 1975 lançam “A Night At The Opera”, considerado o melhor trabalho da carreira deles, este álbum marcou o percurso da banda e definiu um tipo novo de rock, uma fusão de rock com música clássica.
</p>


