    <h1>Garotos Podres</h1>

    <p>
        Garotos Podres é uma banda musical de punk rock que nasceu em 1982 no município de Mauá, Estado de São Paulo.
        
        Em 1985, surgiu o primeiro álbum (em vinil) da banda, o Mais Podre do que Nunca, pela extinta gravadora Rocker e foi o que alavancou a banda, vendendo cerca de 50.000 cópias. Por causa da ditadura, a música "Johnny" foi censurada, e outras, como as "Papai-Noel Filho da Puta" e "Maldita Polícia" foram lançadas como "Papai-Noel Velho Batuta" e "Maldita Preguiça".
    </p>


<p>Em 1985 entraram em estúdio para gravar o que seria uma demo-tape. Foram gravadas e mixadas catorze músicas em doze horas num estúdio de oito canais, e o resultado foi considerado tão bom para os padrões da época que onze destas músicas acabaram se tornando o primeiro álbum da banda.</p>

<p>Em plena ditadura militar, a Censura Federal tentava cumprir o seu papel. Apenas a música (Johnny) foi censurada, sendo proibida a sua execução. As músicas: (Papai-Noel Filho da Puta) e (Maldita Polícia) foram mudadas propositalmente pelos Garotos para burlar a censura. Dessa gravação três músicas foram incluídas na coletânea (Ataque Sonoro) editada pelo selo Ataque Frontal com várias bandas punks brasileiras,entre elas: Ratos de Porão, Cólera, Lobotomia, Grinders, Vírus 27 , etc.</p>



<p>Apesar de estarem na estrada a vinte e seis anos, os Garotos Podres nunca sobreviveram de música, todos os integrantes têm outras atividades que lhes garantem o sustento e a sobrevivência da banda. Isto lhes deu a liberdade de criarem o seu próprio estilo musical e realmente só tocarem o que gostam.</p>




    






